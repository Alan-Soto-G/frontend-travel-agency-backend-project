import { Injectable } from '@angular/core';
import { ToastrService } from 'ngx-toastr';
import { Router } from '@angular/router';
import { AuthService } from '../auth.service';

/**
 * Servicio para autenticaci√≥n con GitHub OAuth
 *
 * Flujo de autenticaci√≥n:
 * 1. Abre popup de GitHub para autorizaci√≥n
 * 2. Usuario autoriza la aplicaci√≥n
 * 3. GitHub devuelve token de autenticaci√≥n
 * 4. Se crea/actualiza usuario en el backend
 * 5. Se guarda sesi√≥n y se redirige al dashboard
 * @version 1.0
 */
@Injectable({
  providedIn: 'root'
})
export class GithubLoginService {

  constructor(
    private toastr: ToastrService,
    private router: Router,
    private authService: AuthService
  ) {}

  /**
   * Inicia el flujo de autenticaci√≥n con GitHub OAuth
   *
   * @returns Promise con el resultado de la autenticaci√≥n
   */
  async login(): Promise<boolean> {
    try {
      console.log('üêô Iniciando login con GitHub...');

      // Llamar al servicio de autenticaci√≥n con GitHub
      const result = await this.authService.loginWithGithub();

      if (result) {
        // ‚úÖ Autenticaci√≥n exitosa
        console.log('‚úÖ Usuario autenticado con GitHub:', result);
        this.toastr.success('¬°Bienvenido!', 'Login con GitHub exitoso');

        // Redirigir al dashboard despu√©s de 2 segundos
        setTimeout(() => {
          this.router.navigate(['/main']);
        }, 2000);

        return true;
      } else {
        // ‚ùå Error en autenticaci√≥n
        this.toastr.error('No se pudo completar la autenticaci√≥n', 'Error con GitHub');
        return false;
      }
    } catch (error) {
      console.error('‚ùå Error en login con GitHub:', error);
      this.toastr.error('No se pudo iniciar sesi√≥n con GitHub. Intenta de nuevo.', 'Error');
      return false;
    }
  }
}
